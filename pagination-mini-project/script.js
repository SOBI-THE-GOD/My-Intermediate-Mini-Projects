const maxDetailLengthChar = 94;
const maxProductPerPage = 10;
const productPagesArr = [];
const $ = document;
const productPagesContainer = $.querySelector(".products-container");
let currentPageIndex = 0;
let lastPageIndex = 0;
const paginationButtonsContainer = $.querySelector(".page-indexes-buttons");
let productAnmNullTimeOut = "";
const toRightPageButton = $.querySelector(".to-right");
const toLeftPageButton = $.querySelector(".to-left");
// three dots
const threeDotsIndex = $.createElement("div");
threeDotsIndex.classList.add("three-dots-index");
threeDotsIndex.innerHTML = "...";
const threeDotsBackground = $.createElement("div");
threeDotsBackground.classList.add("three-dots-background");
const newThreeDotsIndexElem = $.createElement("div");
newThreeDotsIndexElem.classList.add("three-dots-index");
newThreeDotsIndexElem.innerHTML = "...";
const newThreeDotsBackgroundElem = $.createElement("div");
newThreeDotsBackgroundElem.classList.add("three-dots-background");
// three dots created
toRightPageButton.addEventListener("click", goToRightPage);
toLeftPageButton.addEventListener("click", goToLeftPage);
function addToCartButtonClickHandler() {
    let pageIndex = +this.parentElement.parentElement.dataset.pageIndex;
    let productIndex = +this.parentElement.dataset.productIndex;
    let boxIcon = this.querySelector(".box-icon");
    let shoppingCartIcon = this.querySelector(".shopping-cart-icon");
    let addTextSpan = this.querySelector(".add-text");
    boxIcon.style.animation = "shoppingBoxAnm 2s forwards ease-in-out";
    shoppingCartIcon.style.animation =
        "shoppingCartAnm 2s forwards ease-in-out";
    addTextSpan.style.scale = "0";
    setTimeout(() => {
        this.querySelector(".product-quantity-container").style.scale = "1";
    }, 2000);
    this.removeEventListener("click", addToCartButtonClickHandler);
    this.querySelector(".minus").addEventListener(
        "click",
        productAddedToCartCounterHandler
    );
    this.querySelector(".plus").addEventListener(
        "click",
        productAddedToCartCounterHandler
    );
    productPagesArr[pageIndex][productIndex].addedToCartCount = 1;
    this.querySelector(".product-quantity-text").innerHTML = 1;
}
function productAddedToCartCounterHandler() {
    let pageIndex =
        +this.parentElement.parentElement.parentElement.parentElement.dataset
            .pageIndex;
    let productIndex =
        +this.parentElement.parentElement.parentElement.dataset.productIndex;
    if (this.dataset.add === "plus") {
        productPagesArr[pageIndex][productIndex].addedToCartCount += 1;
        this.previousElementSibling.firstElementChild.innerHTML =
            productPagesArr[pageIndex][productIndex].addedToCartCount;
    } else {
        if (productPagesArr[pageIndex][productIndex].addedToCartCount > 1) {
            productPagesArr[pageIndex][productIndex].addedToCartCount -= 1;
        } else {
            productPagesArr[pageIndex][productIndex].addedToCartCount = 0;
            this.parentElement.style.scale = "0";
            this.parentElement.parentElement.children[1].style.animation = null;
            this.parentElement.parentElement.children[2].style.animation = null;
            setTimeout(() => {
                this.parentElement.parentElement.firstElementChild.style.scale =
                    "1";
                this.parentElement.parentElement.addEventListener(
                    "click",
                    addToCartButtonClickHandler
                );
            }, 300);
        }
        this.nextElementSibling.firstElementChild.innerHTML =
            productPagesArr[pageIndex][productIndex].addedToCartCount;
    }
}
function goToRightPage() {
    toRightLeftPageSemiHandler(1, productPagesArr.length - 1);
    pagesIndexDynamicArrangHandler();
}
function goToLeftPage() {
    toRightLeftPageSemiHandler(-1, 0);
    pagesIndexDynamicArrangHandler();
}
function toRightLeftPageSemiHandler(additionNum, minMaxBound) {
    clearTimeout(productAnmNullTimeOut);
    $.querySelector(".products-page").remove();
    lastPageIndex = currentPageIndex;
    if (currentPageIndex !== minMaxBound) {
        currentPageIndex += additionNum;
    }
    productPageCreator(currentPageIndex);
    Array.from($.querySelectorAll(".index-num")).forEach((elem) => {
        if (+elem.dataset.pageIndex === currentPageIndex) {
            elem.style.color = "#ffffff";
            elem.classList.remove("index-num-hover");
        } else if (+elem.dataset.pageIndex === lastPageIndex) {
            elem.style.color = "#6b6b6b";
            elem.classList.add("index-num-hover");
        }
    });
    Array.from($.querySelectorAll(".index-background")).forEach((elem) => {
        if (+elem.dataset.pageIndex === currentPageIndex) {
            elem.style.scale = "1";
        } else if (+elem.dataset.pageIndex === lastPageIndex) {
            elem.style.scale = "0";
        }
    });
}
function pagesIndexDynamicArrangHandler() {
    if (
        (currentPageIndex >= 0 && currentPageIndex <= 3) ||
        (currentPageIndex <= productPagesArr.length - 1 &&
            currentPageIndex >= productPagesArr.length - 4)
    ) {
        removeThreeDotsElems();
        atFirstEndBoundPagesArrangeHandler();
    } else {
        removeThreeDotsElems();
        makeFiveFocusMidPagesBoundVisibleHandler();
    }
}
function removeThreeDotsElems() {
    Array.from($.querySelectorAll(".three-dots-index")).forEach((elem) => {
        elem.remove();
    });
    Array.from($.querySelectorAll(".three-dots-background")).forEach((elem) => {
        elem.remove();
    });
}

function makeFiveFocusMidPagesBoundVisibleHandler() {
    paginationButtonsContainer.insertBefore(
        threeDotsBackground,
        paginationButtonsContainer.children[1]
    );
    $.querySelector(".index-nums-container").insertBefore(
        threeDotsIndex,
        $.querySelector(".index-nums-container").children[1]
    );
    paginationButtonsContainer.insertBefore(
        newThreeDotsBackgroundElem,
        paginationButtonsContainer.children[productPagesArr.length]
    );
    $.querySelector(".index-nums-container").insertBefore(
        newThreeDotsIndexElem,
        $.querySelector(".index-nums-container").children[
            productPagesArr.length
        ]
    );
    for (let i = 1; i < productPagesArr.length - 1; i++) {
        Array.from($.querySelectorAll(".index-num"))[i].style.display = "none";
        Array.from($.querySelectorAll(".index-background"))[i].style.display =
            "none";
    }
    Array.from($.querySelectorAll(".index-background"))[
        currentPageIndex - 2
    ].style.display = "block";
    Array.from($.querySelectorAll(".index-background"))[
        currentPageIndex - 1
    ].style.display = "block";
    Array.from($.querySelectorAll(".index-background"))[
        currentPageIndex
    ].style.display = "block";
    Array.from($.querySelectorAll(".index-background"))[
        currentPageIndex + 1
    ].style.display = "block";
    Array.from($.querySelectorAll(".index-background"))[
        currentPageIndex + 2
    ].style.display = "block";
    Array.from($.querySelectorAll(".index-num"))[
        currentPageIndex - 2
    ].style.display = "flex";
    Array.from($.querySelectorAll(".index-num"))[
        currentPageIndex - 1
    ].style.display = "flex";
    Array.from($.querySelectorAll(".index-num"))[
        currentPageIndex
    ].style.display = "flex";
    Array.from($.querySelectorAll(".index-num"))[
        currentPageIndex + 1
    ].style.display = "flex";
    Array.from($.querySelectorAll(".index-num"))[
        currentPageIndex + 2
    ].style.display = "flex";
}
function productsAssignToEachPageHandler() {
    let fillingPageArr = [];
    productArr.forEach((elem, index) => {
        if (index % maxProductPerPage !== 0 || index === 0) {
            fillingPageArr.push(elem);
        } else if (index % maxProductPerPage === 0) {
            productPagesArr.push(fillingPageArr);
            fillingPageArr = [];
            fillingPageArr.push(elem);
        }
        if (productArr.length - 1 - index === 0) {
            productPagesArr.push(fillingPageArr);
        }
    });
}
function productPageCreator(pageIndex) {
    let newPage = "";
    newPage = $.createElement("div");
    newPage.classList.add("products-page");
    newPage.dataset.pageIndex = pageIndex;
    productPagesArr[pageIndex].forEach((elem, index) => {
        let newProductDetail = elem.productDetail;
        if (elem.productDetail.length > maxDetailLengthChar) {
            newProductDetail =
                elem.productDetail.slice(
                    0,
                    maxDetailLengthChar - elem.productDetail.length
                ) + "...";
        }
        newPage.innerHTML += `
            <div class="product" data-product-index="${index}" style="animation: productPageLoadAnm ${
            0.3 + 0.1 * index
        }s forwards">
                <div 
                    class="product-img" 
                    style="background-image: url(${elem.productImgAddress})"
                ></div>

                <div class="product-about unselectable">
                    <div class="product-name">${elem.productName}</div>
                    <div class="product-detail">
                        ${newProductDetail}
                    </div>
                </div>
                <div class="add-to-cart-container">
                    <span class="add-text unselectable">add</span>
                    <i
                        class="fa-solid fa-cart-shopping shopping-cart-icon unselectable"
                    ></i>
                    <i class="fa-solid fa-box box-icon unselectable"></i>
                    <div class="product-quantity-container">
                        <span class="minus unselectable" data-add="minus">-</span>
                        <div class="product-quantity unselectable">
                            <span class="product-quantity-text">${
                                elem.addedToCartCount
                            }</span>
                        </div>
                        <span class="plus unselectable" data-add="plus">+</span>
                    </div>
                </div>
            </div>`;
    });
    productPagesContainer.append(newPage);
    $.querySelector(".add-to-cart-container").addEventListener(
        "click",
        addToCartButtonClickHandler
    );
    Array.from($.querySelectorAll(".add-to-cart-container")).forEach((elem) => {
        elem.addEventListener("click", addToCartButtonClickHandler);
    });
}
function paginationButtonsGenerator() {
    const indexNumsContainer = $.createElement("div");
    indexNumsContainer.classList.add("index-nums-container");
    productPagesArr.forEach((elem, index) => {
        indexNumsContainer.innerHTML += `<div class="index-num index-num-hover unselectable" data-page-index="${index}">${
            index + 1
        }</div>`;
        paginationButtonsContainer.innerHTML += `<div class="index-background unselectable" data-page-index="${index}"></div>`;
    });
    paginationButtonsContainer.append(indexNumsContainer);
    Array.from($.querySelectorAll(".index-num"))[0].style.color = "#ffffff";
    Array.from($.querySelectorAll(".index-background"))[0].style.scale = "1";
    if (productPagesArr.length > 8) {
        atFirstEndBoundPagesArrangeHandler();
    }
    Array.from($.querySelectorAll(".index-num")).forEach((elem) => {
        elem.addEventListener("click", paginationIndexButtonClickHandler);
    });
}
function atFirstEndBoundPagesArrangeHandler() {
    for (let i = 4; i <= productPagesArr.length - 5; i++) {
        paginationButtonsContainer.children[i].style.display = "none";
        $.querySelector(".index-nums-container").children[i].style.display =
            "none";
    }
    paginationButtonsContainer.insertBefore(
        threeDotsBackground,
        paginationButtonsContainer.children[5]
    );
    $.querySelector(".index-nums-container").insertBefore(
        threeDotsIndex,
        $.querySelector(".index-nums-container").children[5]
    );
    for (let i = 0; i < 4; i++) {
        Array.from($.querySelectorAll(".index-background"))[i].style.display =
            "block";
        Array.from($.querySelectorAll(".index-num"))[i].style.display = "flex";
    }
    for (
        let i = productPagesArr.length - 1;
        i > productPagesArr.length - 5;
        i--
    ) {
        Array.from($.querySelectorAll(".index-background"))[i].style.display =
            "block";
        Array.from($.querySelectorAll(".index-num"))[i].style.display = "flex";
    }
}
function paginationIndexButtonClickHandler(e) {
    clearTimeout(productAnmNullTimeOut);
    $.querySelector(".products-page").remove();
    productPageCreator(+e.target.dataset.pageIndex);
    lastPageIndex = currentPageIndex;
    currentPageIndex = +e.target.dataset.pageIndex;
    Array.from($.querySelectorAll(".index-num")).forEach((elem) => {
        if (+elem.dataset.pageIndex === currentPageIndex) {
            elem.style.color = "#ffffff";
            elem.classList.remove("index-num-hover");
        } else if (+elem.dataset.pageIndex === lastPageIndex) {
            elem.style.color = "#6b6b6b";
            elem.classList.add("index-num-hover");
        }
    });
    Array.from($.querySelectorAll(".index-background")).forEach((elem) => {
        if (+elem.dataset.pageIndex === currentPageIndex) {
            elem.style.scale = "1";
        } else if (+elem.dataset.pageIndex === lastPageIndex) {
            elem.style.scale = "0";
        }
    });
    pagesIndexDynamicArrangHandler();
}
